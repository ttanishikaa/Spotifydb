DROP DATABASE IF EXISTS spotifydb;
-- create table artist
CREATE DATABASE IF NOT EXISTS spotifydb;
USE spotifydb;
CREATE TABLE IF NOT EXISTS artist(
    artist_id INT AUTO_INCREMENT,
    artist_name VARCHAR(100),
    streams BIGINT,
    top_song VARCHAR(100),
    popularity INT,
    top_album VARCHAR(100),
    PRIMARY KEY(artist_id)
);
-- create table album
CREATE TABLE IF NOT EXISTS album(
    album_id INT AUTO_INCREMENT,
    album_name VARCHAR(100) NOT NULL,
    artist_id INT,
    album_rank INT,
    album_genre VARCHAR(100),
    PRIMARY KEY(album_id),
    UNIQUE(album_name),
    FOREIGN KEY (artist_id) REFERENCES artist(artist_id)
);
-- create table song
CREATE TABLE IF NOT EXISTS song(
    track_id INT AUTO_INCREMENT,
    track_name VARCHAR(100) unique,
    artist_id INT,
    track_album_name VARCHAR(100),
    song_genre VARCHAR(100),
    danceability DOUBLE,
    energy DOUBLE,
    song_key INT,
    song_mode INT,
    PRIMARY KEY(track_id),
    FOREIGN KEY(artist_id) references artist(artist_id)
);
-- create table chart
CREATE TABLE IF NOT EXISTS chart(
    chart_id INT AUTO_INCREMENT,
    artist_name VARCHAR(100),
    track_name VARCHAR(100),
    peak_rank INT,
    weeks_on_chart INT,
    danceability DOUBLE,
    energy DOUBLE,
    track_key INT,
    track_mode INT,
    PRIMARY KEY(chart_id)
);
